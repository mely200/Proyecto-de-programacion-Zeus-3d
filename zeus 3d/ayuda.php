<h3>Acerca de los productos</h3>                                     
<p>Los cortantes son realizados en PLA  por medio de la tecnología de impresión 3D.Es un trabajo artesanal, que pasa desde el diseño 2D, modelado 3D, impresión, control calidad y postimpresión.Todo este proceso lleva tiempo y es necesario para garantizar la mejor calidad en nuestros productos.
Imprimimos cada cortador de galletas nosotros mismos y se comprueban para evitar imperfecciones antes del envío. Este artículo es fácil de limpiar, resistente a los niños y  es reutilizable. Limpiarlo con agua tibia y jabon.</p>   
</br>                 
<h3>¿Como comprar?</h3>
<p>Cuando localices el producto deseado, haciendo click en "añadir al carrito" y el producto se añadirá a al "carrito de compra".
En tienda se encuentran todas las categorías que existentes actualmente. Haciendo click en cualquiera de ellas se pueden ver todos los productos que contienen, y accediendo a las sub-categorías se pueden ver las distintas agrupaciones de los mismos productos. Si quieres buscar alguno en concreto, arriba a la derecha tienes un buscador. Introduce su nombre y dale "buscar".
Primero debes seleccionar todos los productos que necesites y agregarlos al carrito. Es recomendable leer las descripciones ya que en ellas encontrarás los tamaños y recomendaciones de uso. Es importante cargar en el carrito el modelo exacto de cortante que necesites. En caso de ser un cortante personalizado, comuníquese con nosotros.
Cuando los productos hayan sido seleccionados en la "cesta de compra" se debe acceder a "carrito"  en el cabecero de la web y allí se podrá eliminar o comprar los productos. </p>
</br>
